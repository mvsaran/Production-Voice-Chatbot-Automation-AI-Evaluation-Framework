"""
Abstract Base Class for Language Model (LLM) Services.

Enables clean architectural decoupling so that standard OpenAI GPT-4o,
Azure OpenAI, or local OSS models can be interchangeably used via configuration.
"""

from abc import ABC, abstractmethod
from typing import List
from app.models.base import ConversationMessage


class BaseLLMService(ABC):
    """
    Abstract interface for conversational Language Models.
    """

    @abstractmethod
    async def generate_response(
        self,
        user_message: str,
        conversation_history: List[ConversationMessage],
        system_prompt: str,
    ) -> str:
        """
        Generate an intelligent conversational response from the LLM.

        Args:
            user_message: The latest transcribed text input from the user.
            conversation_history: Prior chronological turns exchanged in this conversation.
            system_prompt: System-level instructions defining chatbot personality and boundaries.

        Returns:
            The text response generated by the model.

        Raises:
            AuthenticationException: If API keys are invalid.
            NetworkTimeoutException: If API request times out.
            LLMException: For any other generation errors.
        """
        pass
