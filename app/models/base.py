"""
Domain data models and schemas using Pydantic for clean data validation and typing.
"""

from datetime import datetime, timezone
from enum import Enum
from pathlib import Path
from typing import List, Optional
from pydantic import BaseModel, Field, ConfigDict


class RoleEnum(str, Enum):
    """Enumeration of conversational role actors."""
    SYSTEM = "system"
    USER = "user"
    ASSISTANT = "assistant"


class ConversationMessage(BaseModel):
    """Represents a single conversational turn in a dialogue history."""
    model_config = ConfigDict(frozen=True)

    role: RoleEnum = Field(..., description="Role of the speaker (system, user, assistant).")
    content: str = Field(..., description="Text content of the message.")
    timestamp: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
        description="UTC timestamp when the message was recorded or generated."
    )


class ConversationHistory(BaseModel):
    """Complete record of a multi-turn conversation session."""
    conversation_id: str = Field(..., description="Unique identifier for the conversation.")
    created_at: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
        description="Timestamp when conversation session was initialized."
    )
    updated_at: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
        description="Timestamp of the last activity in this session."
    )
    messages: List[ConversationMessage] = Field(
        default_factory=list,
        description="Chronological list of messages exchanged."
    )
    system_prompt: str = Field(..., description="System instructions governing LLM behavior.")


class AudioMetadata(BaseModel):
    """Metadata regarding a recorded or processed audio file."""
    file_path: Path = Field(..., description="Absolute or relative path to the audio file.")
    format: str = Field(default="wav", description="Audio format encoding (e.g., wav, mp3).")
    duration_seconds: Optional[float] = Field(default=None, description="Duration of audio in seconds.")
    file_size_bytes: Optional[int] = Field(default=None, description="Size of the file on disk in bytes.")


class PipelineLatency(BaseModel):
    """Detailed execution time metrics across all pipeline stages in milliseconds."""
    stt_latency_ms: float = Field(default=0.0, description="Time taken for Speech-to-Text transcription.")
    llm_latency_ms: float = Field(default=0.0, description="Time taken for LLM response generation.")
    tts_latency_ms: float = Field(default=0.0, description="Time taken for Text-to-Speech audio synthesis.")
    total_latency_ms: float = Field(default=0.0, description="Total end-to-end pipeline processing time.")


class ChatTurnResponse(BaseModel):
    """Comprehensive response payload returned by the /chat API endpoint."""
    conversation_id: str = Field(..., description="Active conversation identifier.")
    recognized_text: str = Field(..., description="Transcribed text from user input audio.")
    generated_response: str = Field(..., description="Text response generated by the LLM.")
    audio_file_path: str = Field(..., description="File path to the generated TTS output audio.")
    latency: PipelineLatency = Field(..., description="Execution time breakdown across stages.")
    timestamp: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
        description="Completion timestamp of the request."
    )
