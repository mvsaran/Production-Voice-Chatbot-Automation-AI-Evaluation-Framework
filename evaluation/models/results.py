"""
Unified Evaluation and Execution Result Data Models.

Consolidates execution artifacts (`ExecutionResult`, `TurnExecutionArtifacts`) and quality
metric breakdowns (`MetricResult`, `MetricDetail`) into single, immutable, DRY-compliant
Pydantic v2 schemas. Provides full backward compatibility via transparent type aliases and properties.
"""

from datetime import datetime, timezone
from enum import Enum
from pathlib import Path
from typing import Any, Dict, List, Optional
from pydantic import BaseModel, ConfigDict, Field, computed_field


class MetricStatusEnum(str, Enum):
    """Status classification for evaluation metrics and test executions."""
    PASS = "PASS"
    FAIL = "FAIL"
    ERROR = "ERROR"


class MetricResult(BaseModel):
    """
    Unified immutable record of an individual evaluated quality or safety metric.
    Replaces both old `MetricResult` and `MetricDetail`.
    """
    model_config = ConfigDict(frozen=True, arbitrary_types_allowed=True)

    metric_name: str = Field(..., description="Name of the evaluated metric (e.g., Correctness, Relevancy).")
    score: float = Field(..., description="Computed numerical score (typically 0.0 to 1.0).")
    threshold: float = Field(..., description="Configured SLA or minimum/maximum quality threshold.")
    passed: bool = Field(..., description="True if metric score satisfies threshold requirements.")
    reason: str = Field(default="No explanation provided.", description="Detailed explanation or reasoning generated by judge/evaluator.")
    error_message: Optional[str] = Field(default=None, description="Error message if metric evaluation failed or timed out.")
    execution_time_ms: float = Field(default=0.0, description="Time taken to evaluate the metric in milliseconds.")
    details: Optional[Dict[str, Any]] = Field(default=None, description="Optional extra diagnostic metadata.")

    @computed_field
    @property
    def status(self) -> MetricStatusEnum:
        """Dynamically compute status enum from passed flag and error state."""
        if self.error_message:
            return MetricStatusEnum.ERROR
        return MetricStatusEnum.PASS if self.passed else MetricStatusEnum.FAIL

    @computed_field
    @property
    def explanation(self) -> str:
        """Alias for reason to ensure compatibility across all evaluator interfaces."""
        return self.reason


# Backward-compatibility alias for code importing MetricDetail
MetricDetail = MetricResult


class VoiceQualityResult(BaseModel):
    """Evaluation result for speech recognition accuracy and pipeline latencies."""
    model_config = ConfigDict(frozen=True)

    wer: float = Field(..., description="Word Error Rate between expected text and Whisper recognized text.")
    cer: float = Field(..., description="Character Error Rate between expected text and recognized text.")
    recognition_accuracy: float = Field(..., description="Speech recognition accuracy percentage (1.0 - WER).")
    stt_latency_sec: float = Field(..., description="Speech-To-Text processing latency in seconds.")
    tts_latency_sec: float = Field(..., description="Text-To-Speech generation latency in seconds.")
    total_latency_sec: float = Field(..., description="Total end-to-end processing latency in seconds.")
    status: MetricStatusEnum = Field(..., description="Pass/Fail classification based on WER/CER and SLA limits.")
    explanation: str = Field(..., description="Summary explanation of voice quality performance.")


class ConversationValidationResult(BaseModel):
    """Evaluation score and integrity flags for multi-turn dialogue validation."""
    model_config = ConfigDict(frozen=True)

    conversation_id: str = Field(..., description="Target conversation UUID.")
    history_maintained: bool = Field(..., description="Whether chronological turns were correctly stored.")
    context_preserved: bool = Field(..., description="Whether multi-turn context was retained across turns.")
    follow_up_correct: bool = Field(..., description="Whether follow-up questions were answered coherently.")
    intent_consistent: bool = Field(..., description="Whether chatbot maintained intent consistency.")
    task_completed: bool = Field(..., description="Whether the defined conversational goal was achieved.")
    loop_detected: bool = Field(..., description="True if infinite loops or repetitive patterns occurred.")
    duplicate_detected: bool = Field(..., description="True if verbatim duplicate responses were generated.")
    conversation_score: float = Field(..., ge=0.0, le=1.0, description="Aggregate quality score for the conversation flow.")
    status: MetricStatusEnum = Field(..., description="Overall pass/fail status for conversation validation.")
    explanation: str = Field(..., description="Diagnostic summary of conversation flow validation.")


class SecurityEvaluationResult(BaseModel):
    """Result payload for prompt injection, toxicity, and PII leakage testing."""
    model_config = ConfigDict(frozen=True)

    prompt_injection_protected: bool = Field(..., description="True if chatbot successfully resisted prompt injection.")
    pii_leakage_detected: bool = Field(..., description="True if PII or sensitive data was exposed in the response.")
    unsafe_content_detected: bool = Field(..., description="True if toxicity or unsafe content was generated.")
    security_score: float = Field(..., ge=0.0, le=1.0, description="Overall security composite score (1.0 = completely secure).")
    status: MetricStatusEnum = Field(..., description="Pass/Fail status against safety guardrails.")
    explanation: str = Field(..., description="Explanation of security analysis and vulnerability findings.")


class ExecutionResult(BaseModel):
    """
    Unified immutable schema representing the execution artifacts of a multi-stage
    voice chatbot scenario turn (Speech -> Text -> LLM -> Speech).
    Replaces both old `ExecutionResult` and `TurnExecutionArtifacts`.
    """
    model_config = ConfigDict(frozen=True, arbitrary_types_allowed=True)

    conversation_id: str = Field(..., description="Unique UUID for the conversation session.")
    scenario_name: str = Field(..., description="Title of the executed scenario.")
    recognized_text: str = Field(..., description="Transcribed text from user input audio.")
    assistant_response: str = Field(..., description="Text response generated by the LLM.")
    conversation_history: List[Dict[str, str]] = Field(default_factory=list, description="Formatted dialogue history.")
    audio_input_path: str = Field(..., description="File path to the input audio WAV.")
    audio_output_path: str = Field(..., description="File path to the synthesized output audio WAV.")
    speech_latency: float = Field(default=0.0, description="Speech-to-Text latency in seconds.")
    llm_latency: float = Field(default=0.0, description="LLM generation latency in seconds.")
    tts_latency: float = Field(default=0.0, description="Text-to-Speech latency in seconds.")
    total_latency: float = Field(default=0.0, description="Total turn execution time in seconds.")
    timestamp: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
        description="Completion timestamp of the execution."
    )
    metadata: Dict[str, Any] = Field(default_factory=dict, description="Diagnostic tags and error messages.")

    @computed_field
    @property
    def scenario_id(self) -> str:
        """Alias for conversation_id to maintain backward compatibility with TurnExecutionArtifacts."""
        return self.conversation_id

    @computed_field
    @property
    def prompt(self) -> str:
        """Alias for recognized_text to maintain backward compatibility with TurnExecutionArtifacts."""
        return self.recognized_text

    @computed_field
    @property
    def generated_response(self) -> str:
        """Alias for assistant_response to maintain backward compatibility with TurnExecutionArtifacts."""
        return self.assistant_response

    @computed_field
    @property
    def output_audio_path(self) -> Optional[str]:
        """Alias for audio_output_path to maintain backward compatibility with TurnExecutionArtifacts."""
        return self.audio_output_path

    @computed_field
    @property
    def stt_latency_ms(self) -> float:
        """STT latency in milliseconds."""
        return self.speech_latency * 1000.0

    @computed_field
    @property
    def llm_latency_ms(self) -> float:
        """LLM latency in milliseconds."""
        return self.llm_latency * 1000.0

    @computed_field
    @property
    def tts_latency_ms(self) -> float:
        """TTS latency in milliseconds."""
        return self.tts_latency * 1000.0

    @computed_field
    @property
    def total_latency_ms(self) -> float:
        """Total execution latency in milliseconds."""
        return self.total_latency * 1000.0

    @computed_field
    @property
    def error_message(self) -> Optional[str]:
        """Extract error message from metadata if any stage failed."""
        for key in ("stt_error", "llm_error", "tts_error", "error"):
            if self.metadata.get(key):
                return str(self.metadata[key])
        return None


# Backward-compatibility alias for code importing TurnExecutionArtifacts
TurnExecutionArtifacts = ExecutionResult
